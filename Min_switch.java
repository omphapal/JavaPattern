package feb2;

public class Min_switch {

	public static void main(String[] args) {
		
		int num=-9;
		
		switch(num) {
		case 1-5:
			System.out.println("www.Gmail.Com");
		break;
		case 6-15:
			System.out.println("www.google.com");
		break;
		
		default:
			System.out.println("Wrong option");
			break;
			
		}
		
	}
}
